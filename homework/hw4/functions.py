from math import *

def prod (a, b):
  ''' returns the product of a and b '''
  return a * b

def cos_pi (a):
  ''' returns the cosine of the product of pi and a '''
  return cos(pi * a)

def sin_pi (a):
  ''' returns the sine of the product of pi and a '''
  return sin(pi * a)

def x (x, _):
  ''' returns the x value of an x, y coordinate '''
  return x

def y (_, y):
  ''' returns the y value of an x, y coordinate '''
  return y

